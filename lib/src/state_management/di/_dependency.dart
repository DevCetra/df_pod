/// A class representing a registered dependency with an optional [unregister]
/// callback.
final class Dependency<T> {
  final T value;
  final DIKey key;
  final void Function()? unregister;

  const Dependency(
    this.value, {
    this.key = DIKey.defaultKey,
    this.unregister,
  });
}
